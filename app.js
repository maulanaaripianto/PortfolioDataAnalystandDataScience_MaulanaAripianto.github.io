function getStorage(key, fallback) {
  try { return localStorage.getItem(key) || fallback; } catch { return fallback; }
}

function setStorage(key, value) {
  try { localStorage.setItem(key, value); } catch {}
}

function setText(id, value) {
  const el = document.getElementById(id);
  if (el && value !== undefined) el.textContent = value;
}

function setList(id, items = []) {
  const el = document.getElementById(id);
  if (!el) return;
  el.innerHTML = "";
  items.forEach((item) => {
    const li = document.createElement("li");
    li.textContent = item;
    el.appendChild(li);
  });
}

function updateMeta(t) {
  document.title = t.pageTitle;
  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc) metaDesc.setAttribute("content", t.pageDescription);
}

function startBrandTyping() {
  const textEl = document.getElementById("brandTypingText");
  if (!textEl) return;
  const reduceMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;
  const text = "Maulana Aripianto";
  clearTimeout(typingTimeout);
  if (reduceMotion) { textEl.textContent = text; return; }
  
  let index = 0;
  textEl.textContent = "";
  function typeLoop() {
    if (index <= text.length) {
      textEl.textContent = text.slice(0, index);
      index += 1;
      typingTimeout = setTimeout(typeLoop, 90);
    }
  }
  typeLoop();
}

function renderCertificates(certificates = []) {
  const grid = document.getElementById("certGrid");
  if (!grid) return;
  grid.innerHTML = "";
  certificates.forEach((cert, index) => {
    const card = document.createElement("article");
    card.className = "cert-card reveal";
    if (index % 3 === 1) card.classList.add("delay-1");
    if (index % 3 === 2) card.classList.add("delay-2");

    card.innerHTML = `
      <div class="cert-header">
        <div class="cert-ribbon"></div>
        <h3>${cert.title}</h3>
      </div>
      <div class="cert-img-wrap">
        <img src="${cert.img}" alt="Sertifikat ${cert.title}">
      </div>
      <a href="${cert.link}" target="_blank" rel="noopener noreferrer" class="cert-btn">CLICK HERE</a>
    `;
    grid.appendChild(card);
  });
  observeReveal(grid.querySelectorAll(".reveal"));
}

function createNewProjectCard(project, index) {
  const card = document.createElement("article");
  card.className = "project-card reveal";
  if (index === 1) card.classList.add("delay-1");
  if (index === 2) card.classList.add("delay-2");

  card.innerHTML = `
    <div class="project-img-wrap">
      <span class="project-badge">Project ${index + 1}</span>
      <img src="${project.img}" alt="${project.title}">
    </div>
    <h3>${project.title}</h3>
    <p>${project.desc}</p>
    <a href="${project.link}" target="_blank" rel="noopener noreferrer" class="project-link-btn">
      🔗 View Gdrive
    </a>
  `;
  return card;
}

function renderProjects(projectsDA = [], projectsDS = []) {
  const daGrid = document.getElementById("daProjectGrid");
  const dsGrid = document.getElementById("dsProjectGrid");
  
  if (daGrid) {
    daGrid.innerHTML = "";
    projectsDA.forEach((proj, idx) => daGrid.appendChild(createNewProjectCard(proj, idx)));
  }
  if (dsGrid) {
    dsGrid.innerHTML = "";
    projectsDS.forEach((proj, idx) => dsGrid.appendChild(createNewProjectCard(proj, idx)));
  }
  observeReveal(document.querySelectorAll(".reveal"));
}

function openPdfModal(type) {
  const modal = document.getElementById("pdfModal");
  const iframe = document.getElementById("pdfIframe");
  const modalTitle = document.getElementById("modalTitle");
  
  const currentData = content[currentLang];
  const pdfUrl = currentData.pdfs[type];

  modalTitle.textContent = type === 'da' ? currentData.btnPdfDa : currentData.btnPdfDs;
  iframe.src = pdfUrl;
  modal.style.display = "block";
  document.body.style.overflow = "hidden"; 
}

function closePdfModal() {
  const modal = document.getElementById("pdfModal");
  const iframe = document.getElementById("pdfIframe");
  modal.style.display = "none";
  iframe.src = ""; 
  document.body.style.overflow = "auto"; 
}

window.onclick = function(event) {
  const modal = document.getElementById("pdfModal");
  if (event.target == modal) {
    closePdfModal();
  }
}

function updateLanguageToggle(lang) {
  const langToggle = document.getElementById("langToggle");
  if (!langToggle) return;
  langToggle.textContent = lang === "id" ? "EN" : "ID";
  langToggle.setAttribute("aria-label", lang === "id" ? "Ganti bahasa ke English" : "Switch language to Bahasa Indonesia");
}

function updateThemeToggle(theme) {
  const themeToggle = document.getElementById("themeToggle");
  if (!themeToggle) return;
  themeToggle.textContent = theme === "dark" ? "☀" : "🌙";
  themeToggle.setAttribute("aria-label", theme === "dark" ? "Switch to light mode" : "Switch to dark mode");
}

function applyLanguage(lang) {
  currentLang = lang;
  setStorage("lang", lang);
  const t = content[lang];
  document.documentElement.lang = lang;
  updateMeta(t);

  textKeys.forEach((key) => setText(key, t[key]));
  listKeys.forEach((key) => setList(key, t[key]));

  renderProjects(t.projectsDA, t.projectsDS);
  renderCertificates(t.certificates);
  updateLanguageToggle(lang);
}

function applySavedTheme() {
  const savedTheme = getStorage("theme", "dark");
  document.body.setAttribute("data-theme", savedTheme);
  updateThemeToggle(savedTheme);
}

function toggleTheme() {
  const currentTheme = document.body.getAttribute("data-theme");
  const nextTheme = currentTheme === "dark" ? "light" : "dark";
  document.body.setAttribute("data-theme", nextTheme);
  setStorage("theme", nextTheme);
  updateThemeToggle(nextTheme);
}

function handleNavbarShrink() {
  const header = document.getElementById("header");
  if (header) header.classList.toggle("shrink", window.scrollY > 40);
}

function observeReveal(targets = document.querySelectorAll(".reveal")) {
  const reduceMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;
  if (reduceMotion || !("IntersectionObserver" in window)) {
    targets.forEach((el) => el.classList.add("active"));
    return;
  }
  if (!revealObserver) {
    revealObserver = new IntersectionObserver((entries, observer) => {
        entries.forEach((entry) => {
          if (entry.isIntersecting) {
            entry.target.classList.add("active");
            observer.unobserve(entry.target);
          }
        });
      }, { threshold: 0.12 });
  }
  targets.forEach((el) => {
    if (!el.classList.contains("active")) revealObserver.observe(el);
  });
}

function setupMobileNav() {
  const navToggle = document.getElementById("navToggle");
  const navMenu = document.getElementById("navMenu");
  const navLinks = navMenu ? navMenu.querySelectorAll("a") : [];
  if (!navToggle || !navMenu) return;

  navToggle.addEventListener("click", () => {
    const isOpen = navMenu.classList.toggle("open");
    navToggle.setAttribute("aria-expanded", String(isOpen));
  });
  navLinks.forEach((link) => {
    link.addEventListener("click", () => {
      navMenu.classList.remove("open");
      navToggle.setAttribute("aria-expanded", "false");
    });
  });
}

function setupActiveNav() {
  const sections = document.querySelectorAll("main section[id]");
  const navLinks = document.querySelectorAll(".nav-menu a");
  if (!sections.length || !navLinks.length) return;

  const activateLink = () => {
    let currentId = "home";
    sections.forEach((section) => {
      const top = section.offsetTop - 130;
      if (window.scrollY >= top) currentId = section.getAttribute("id");
    });
    navLinks.forEach((link) => {
      const href = link.getAttribute("href");
      link.classList.toggle("active", href === `#${currentId}`);
    });
  };
  activateLink();
  window.addEventListener("scroll", activateLink);
}

document.addEventListener("DOMContentLoaded", () => {
  applySavedTheme();
  applyLanguage(getStorage("lang", "id"));
  observeReveal();
  handleNavbarShrink();
  startBrandTyping();
  setupMobileNav();
  setupActiveNav();

  document.getElementById("langToggle")?.addEventListener("click", () => applyLanguage(currentLang === "id" ? "en" : "id"));
  document.getElementById("themeToggle")?.addEventListener("click", toggleTheme);
  window.addEventListener("scroll", handleNavbarShrink);
});